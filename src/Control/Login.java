    private void entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarActionPerformed
        if (login.getText().trim().equalsIgnoreCase("") || senha.getText().trim().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos!", "ATENÇÂO", JOptionPane.WARNING_MESSAGE);
        } else {
            Connection conexao = Conexao.conectar();
            UsuarioDAO usuarioDAO = new UsuarioDAO(conexao);
            EmpresaDAO empresaDAO = new EmpresaDAO(conexao);
            if (usuarioDAO.logarUsuario(login.getText(), senha.getText())) {
                if (Usuario.getAtivoUser() == 1) {
                    this.dispose();
                    usuarioDAO.onlineUsuario(true);
                    Usuario.setInicioAcesso(new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date()));
                    switch (Usuario.getNivelUser()) {
                        case 0:
                            empresaDAO.carregaDadosEmpresa();
                            Caixa caixa = new Caixa();
                            caixa.setVisible(true);
                            break;
                        case 1:
                            empresaDAO.carregaDadosEmpresa();
                            Home home = new Home();
                            home.setVisible(true);
                            break;
                    }
                }else if (Usuario.getAtivoUser() == 0) {
                    limpar();
                    JOptionPane.showMessageDialog(rootPane, "Usuário inativo!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                    login.requestFocus();
                }
            } else {
                limpar();
                JOptionPane.showMessageDialog(rootPane, "Login e/ou Senha incorretos ou inexistentes!", "ERRO", JOptionPane.ERROR_MESSAGE);
                login.requestFocus();
            }
            Conexao.desconectar(conexao);
        }
    }//GEN-LAST:event_entrarActionPerformed


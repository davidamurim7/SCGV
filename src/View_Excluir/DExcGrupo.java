    private void confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarActionPerformed
        if (selecionador.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Grupo!", "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            Connection conexao = Conexao.conectar();
            GrupoDAO grupoDAO = new GrupoDAO(conexao);
            Grupo grupo = new Grupo();
            grupo.setNome((String) selecionador.getSelectedItem());
            if(grupoDAO.delGrupo(grupo)){
                limpar();
                telaGrupos.preencheTabela();
                selecionador.removeItem(grupo.getNome());
                JOptionPane.showMessageDialog(rootPane, "Grupo excluído com sucesso!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(rootPane, "Verifique se o Grupo não está sendo usado pelo sistema!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
            Conexao.desconectar(conexao);
        }
    }//GEN-LAST:event_confirmarActionPerformed


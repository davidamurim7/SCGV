    public void preencheFornecedor(){
        Connection conexao = Conexao.conectar();
        FornecedorDAO fornecedorDAO = new FornecedorDAO(conexao);
        List<Fornecedor> listaFornecedores = new ArrayList<>();
        listaFornecedores = fornecedorDAO.listarFornecedor();
        for (Fornecedor f : listaFornecedores) {
            selecionador.addItem(f.getNome());
        }
        Conexao.desconectar(conexao);
    }
    
    public void preencheCampos(){
        if(selecionador.getSelectedIndex() == 0){
            limpar();
        }else{
            Connection conexao = Conexao.conectar();
            FornecedorDAO fornecedorDAO = new FornecedorDAO(conexao);
            Fornecedor fornecedor = fornecedorDAO.selecionarFornecedor(fornecedorDAO.retornaIdFornecedor((String) selecionador.getSelectedItem()));
            telefone.setText(fornecedor.getTelefone());
            cnpj.setText(fornecedor.getCnpj());
            endereco.setText(fornecedor.getEndereco());
            Conexao.desconectar(conexao);
        }
    }
    
    public void limpar(){
        selecionador.setSelectedIndex(0);
        telefone.setText("");
        cnpj.setText("");
        endereco.setText("");
    }


    private void confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarActionPerformed
        if (selecionador.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Funcionário!", "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            Connection conexao = Conexao.conectar();
            FuncionarioDAO funcionarioDAO = new FuncionarioDAO(conexao);
            Funcionario funcionario = new Funcionario();
            funcionario.setNome((String) selecionador.getSelectedItem());
            if(funcionarioDAO.delFuncionario(funcionario)){
                Historico historico = new Historico(
                    0, 
                    new SimpleDateFormat("yyyy-MM-dd").format(new Date()), 
                    3, 
                    "O funcionário \""+funcionario.getNome()+"\" foi EXCLUÍDO.", 
                    Usuario.getIdUser()
                );
                HistoricoDAO historicoDAO = new HistoricoDAO(conexao);
                historicoDAO.addHistorico(historico);
                limpar();
                telaFuncionarios.preencheTabela();
                selecionador.removeItem(funcionario.getNome());
                JOptionPane.showMessageDialog(rootPane, "Funcionário excluído com sucesso!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(rootPane, "Verifique se o Funcionário não está sendo usado pelo sistema!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
            Conexao.desconectar(conexao);
        }
    }//GEN-LAST:event_confirmarActionPerformed


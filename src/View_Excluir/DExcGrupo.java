    public void preencheGrupos(){
        Connection conexao = Conexao.conectar();
        GrupoDAO grupoDAO = new GrupoDAO(conexao);
        List<Grupo> listaGrupo = new ArrayList<>();
        listaGrupo = grupoDAO.listarGrupo();
        for (Grupo g : listaGrupo) {
            selecionador.addItem(g.getNome());
        }
        Conexao.desconectar(conexao);
    }
    
    public void preencheCampos(){
        if(selecionador.getSelectedIndex() == 0){
            limpar();
        }else{
            Connection conexao = Conexao.conectar();
            GrupoDAO grupoDAO = new GrupoDAO(conexao);
            Grupo grupo = grupoDAO.selecionarGrupo(grupoDAO.retornaIdGrupo((String) selecionador.getSelectedItem()));
            descricao.setText(grupo.getDescricao());
            Conexao.desconectar(conexao);
        }
    }
    
    public void limpar(){
        selecionador.setSelectedIndex(0);
        descricao.setText("");
    }


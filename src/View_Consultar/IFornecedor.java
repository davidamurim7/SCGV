    public void preencheTabela(){
        Connection conexao = Conexao.conectar();
        FornecedorDAO fornecedorDAO = new FornecedorDAO(conexao);
        List<Fornecedor> listaFornecedores = new ArrayList<>();
        DefaultTableModel model = (DefaultTableModel) tabFornecedores.getModel();
        while(model.getRowCount() > 0)
            model.removeRow(0);
        int i = 0;
        listaFornecedores = fornecedorDAO.listarFornecedor();
        for(Fornecedor f : listaFornecedores){
            if(f.getNome().contains(filtro)){
                model.addRow(new String[i]);
                tabFornecedores.setValueAt(f.getNome(), i, 0);
                tabFornecedores.setValueAt(f.getEndereco(), i, 1);
                tabFornecedores.setValueAt(f.getTelefone(), i, 2);
                tabFornecedores.setValueAt(f.getCnpj(), i, 3);
                i++;
            }
        }
        Conexao.desconectar(conexao);
    }


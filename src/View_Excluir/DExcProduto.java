    public void preencheCampos(String codigo){
        codigoT.setForeground(Color.black);
        codigoProd = codigo;
        Connection conexao = Conexao.conectar();
        ProdutoDAO produtoDAO = new ProdutoDAO(conexao);
        Produto produto = produtoDAO.selecionarProduto(codigo);
        nomeT.setText(produto.getNome());
        marcaT.setText(produto.getMarca());
        precoT.setText(String.valueOf(produto.getPreco()).replace(".", ","));
        custoT.setText(String.valueOf(produto.getCusto()).replace(".", ","));
        quantidadeT.setText(String.valueOf(produto.getQuantidade()));
        grupoT.setText(new GrupoDAO(conexao).selecionarGrupo(produto.getIdGrupo()).getNome());
        entregaT.setText(new SimpleDateFormat("dd/MM/yyyy").format(produto.getDataEntrega()));
        if(produto.getDataValidade() == null)
            validadeT.setText("Sem validade");
        else
            validadeT.setText(new SimpleDateFormat("dd/MM/yyyy").format(produto.getDataValidade()));
        fornecedorT.setText(new FornecedorDAO(conexao).selecionarFornecedor(produto.getIdFornecedor()).getNome());
        descricaoT.setText(produto.getDescricao());
        Conexao.desconectar(conexao);
    }
    

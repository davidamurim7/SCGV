    public void preencheFuncionario(){
        Connection conexao = Conexao.conectar();
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO(conexao);
        List<Funcionario> listaFuncionario = new ArrayList<>();
        listaFuncionario = funcionarioDAO.listarFuncionario();
        for (Funcionario f : listaFuncionario) {
            if(f.getId() != Usuario.getIdUser())//Lista todos os funcionários exeto o proprio usuario do sistema
                selecionador.addItem(f.getNome());
        }
        Conexao.desconectar(conexao);
    }
    
    public void preencheCampos(){
        if(selecionador.getSelectedIndex() == 0){
            limpar();
        }else{
            Connection conexao = Conexao.conectar();
            FuncionarioDAO funcionarioDAO = new FuncionarioDAO(conexao);
            Funcionario funcionario = funcionarioDAO.selecionarFuncionario(funcionarioDAO.retornaIdFuncionario((String) selecionador.getSelectedItem()));
            cpf.setText(funcionario.getCpf());
            telefone.setText(funcionario.getTelefone());
            endereco.setText(funcionario.getEndereco());
            if(funcionario.getAtivo() == 0)
                ativo.setText("NÃO");
            else
                ativo.setText("SIM");
            email.setText(funcionario.getEmail());
            if(funcionario.getNivel()== 0)
                nivel.setText("CAIXA");
            else
                nivel.setText("ADMINISTRADOR");
            Conexao.desconectar(conexao);
        }
    }
    
    public void limpar(){
        selecionador.setSelectedIndex(0);
        cpf.setText(" - ");
        telefone.setText(" - ");
        endereco.setText(" - ");
        email.setText(" - ");
        ativo.setText(" - ");
        nivel.setText(" - ");
        selecionador.requestFocus();
    }

